#include<minishell_parsing.h>

void	sigfunc(int signum)
{
	printf("aapjes, %i", signum);
	exit(signum);
}

int	main(void)
{
	char	*line;

	signal(SIGINT, sigfunc);
	while (true)
	{
		line = readline("KAAS% ");
		if (!line)
			return (printf("exit\n"), 1);
		if (line[0] != '\0')
			add_history(line);
		if (ft_strncmp(line, "exit", 4) == 0)
			return (free(line), 0);
		else
		{
			printf("line = %s\n", line);
			free(line);
		}
	}
}
